// Regenerate lib/chat/genui-suffix.ts from OpenUI's prebuilt chat library.
//
// Run from the platform package root:  node scripts/gen-genui-suffix.mjs
//
// Why precompute: <Renderer> (client) needs the OpenUI component library, but the system
// prompt it implies is static. We bake that prompt tail into a string so server route
// handlers import only a string — importing @openuidev/react-ui server-side pulls in
// recharts/radix/react-dom (and "use client" modules) and fails the Next.js server build.
import { openuiChatLibrary } from "@openuidev/react-ui/genui-lib";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

// House rules for how the model should answer. Keep these in sync with the product's
// desired answer style, then re-run this script.
const GENUI_RULES = [
  "Respond by composing generative-UI components from the library — never plain markdown or raw prose paragraphs.",
  "Never emit markdown syntax (no '#' headings, '**bold**', or '| … |' tables). Use the library's heading, text, card, table, list, and callout components instead.",
  "Use a table to compare frameworks/standards, cards to group related facts, and a callout to flag mandatory-vs-voluntary requirements or cautions.",
  "End an answer with 1–3 relevant follow-up questions as actionable items the user can click to continue the conversation.",
  "Keep tool behavior unchanged: when the user wants to draft a data request, still call the draftDataRequest tool.",
];

// openuiChatLibrary.prompt() returns `preamble + SUFFIX` for any preamble, so generate with
// a placeholder preamble and strip it — leaving the preamble-independent tail.
const PLACEHOLDER = "__PREAMBLE__";
const full = openuiChatLibrary.prompt({ preamble: PLACEHOLDER, additionalRules: GENUI_RULES });
if (!full.startsWith(PLACEHOLDER)) throw new Error("Unexpected prompt shape — preamble is not a prefix");
const suffix = full.slice(PLACEHOLDER.length);

const out = join(dirname(fileURLToPath(import.meta.url)), "..", "lib", "chat", "genui-suffix.ts");
const header =
  "// AUTO-GENERATED by scripts/gen-genui-suffix.mjs — do not edit by hand.\n" +
  "// The static tail of openuiChatLibrary.prompt(): OpenUI Lang syntax + the component\n" +
  "// catalog + the GreenMentor generative-UI rules. Kept precomputed so server route\n" +
  "// handlers never import the OpenUI client component library (which would break the\n" +
  "// Next.js server build). Regenerate after changing the rules or bumping @openuidev/react-ui.\n";
writeFileSync(out, header + "export const GENUI_SUFFIX = " + JSON.stringify(suffix) + ";\n");
console.log("wrote", out, "(" + suffix.length + " chars)");
